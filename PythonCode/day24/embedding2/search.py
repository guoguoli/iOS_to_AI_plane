"""
暴力搜索（Flat Index）的时间复杂度分析

假设：
- 数据集大小 N = 1,000,000（百万级题库）
- 向量维度 D = 1536（通义千问text-embedding-v3）
- 查询向量 q ∈ R^1536

单次查询：
- 计算量 = N × D = 1,000,000 × 1,536 = 1.536 × 10^9 次乘加
- 以CPU 10^9 FLOPS估算 ≈ 1.5秒/查询
- 实际考虑内存访问延迟 ≈ 2-5秒/查询

QPS（每秒查询数）≈ 0.2-0.5

这完全不可接受！
- 教育科技场景：学生搜索题目，响应时间需 < 100ms
- 医疗健康场景：实时病历匹配，需 < 50ms

解决方案：近似最近邻搜索（ANN）
- 牺牲少量精度（recall 95%+），换取数量级的速度提升
- 从 O(N×D) → O(log(N)×D) 或 O(√N×D)
"""

