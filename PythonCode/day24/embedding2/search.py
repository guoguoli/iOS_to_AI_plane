"""
暴力搜索（Flat Index）的时间复杂度分析

假设：
- 数据集大小 N = 1,000,000（百万级题库）
- 向量维度 D = 1536（通义千问text-embedding-v3）
- 查询向量 q ∈ R^1536

单次查询：
- 计算量 = N × D = 1,000,000 × 1,536 = 1.536 × 10^9 次乘加
- 以CPU 10^9 FLOPS估算 ≈ 1.5秒/查询
- 实际考虑内存访问延迟 ≈ 2-5秒/查询

QPS（每秒查询数）≈ 0.2-0.5

这完全不可接受！
- 教育科技场景：学生搜索题目，响应时间需 < 100ms
- 医疗健康场景：实时病历匹配，需 < 50ms

解决方案：近似最近邻搜索（ANN）
- 牺牲少量精度（recall 95%+），换取数量级的速度提升
- 从 O(N×D) → O(log(N)×D) 或 O(√N×D)
"""

# 类比iOS开发中的索引
"""
iOS Core Data中的索引原理类似：

没有索引（Flat搜索）：
  SELECT * FROM Question WHERE content LIKE '%二次函数%'
  → 全表扫描，O(N)，10万条数据需要数秒

有索引（B-Tree/倒排索引）：
  SELECT * FROM Question WHERE content LIKE '%二次函数%'
  → 索引查找，O(log N)，毫秒级

向量索引 vs 传统索引的区别：
┌────────────────────┬─────────────────┬──────────────────┐
│                    │ 传统索引         │ 向量索引          │
├────────────────────┼─────────────────┼──────────────────┤
│ 查询方式           │ 精确匹配        │ 相似度匹配        │
│ 索引结构           │ B-Tree / 哈希表  │ 图 / 聚类 / 哈希  │
│ 结果确定性         │ 100%精确        │ 近似（95%+召回）   │
│ 适合维度           │ 低维（<10）      │ 高维（>100）       │
│ iOS类比            │ CoreData索引     │ 需要专用向量库     │
└────────────────────┴─────────────────┴──────────────────┘
"""
"""
向量索引选型决策树

                    ┌─ 数据量 < 10万？
                    │   ├─ 是 → Flat（暴力索引，精确且够快）
                    │   └─ 否 ↓
                    │
                    ├─ 内存是否充裕？
                    │   ├─ 是 → HNSW（精度最高、速度最快）
                    │   └─ 否 ↓
                    │
                    ├─ 需要增量更新？
                    │   ├─ 频繁更新 → LSH / IVF（增量友好）
                    │   └─ 批量构建 → HNSW（构建质量更高）
                    │
                    ├─ 精度要求？
                    │   ├─ 极高（>99%）→ HNSW + 大ef
                    │   ├─ 较高（>95%）→ HNSW / IVF+PQ
                    │   └─ 一般（>90%）→ IVF / LSH
                    │
                    ├─ 延迟要求？
                    │   ├─ <1ms → HNSW
                    │   ├─ <10ms → IVF / HNSW
                    │   └─ <100ms → IVF+PQ / LSH
                    │
                    └─ 成都产业场景推荐
                        ├─ 教育科技题库 → HNSW（精度优先）
                        ├─ 医疗病历检索 → IVF+PQ（隐私+规模）
                        └─ 工业知识库 → HNSW+PQ（平衡方案）
"""

# 产业场景具体推荐
"""
1. 教育科技百万题库（成都某K12教育公司）
   - 数据量：200万+题目
   - 特点：题目短、更新频繁、精度要求高
   - 推荐：HNSW（M=32, efConstruction=200）
   - 理由：学生搜题需要高精度，HNSW延迟最低
   - 备选：IVF-HNSW（超大规模时）

2. 医疗电子病历（成都某三甲医院）
   - 数据量：500万+病历
   - 特点：隐私敏感、长文本、增量更新
   - 推荐：IVF-PQ（nlist=4096, m=32）
   - 理由：内存受限、需量化压缩保护隐私
   - 备选：本地部署Milvus + IVF-PQ

3. 工业设备知识库（成都某制造企业）
   - 数据量：50万+文档
   - 特点：专业术语多、查询模式固定
   - 推荐：HNSW-PQ（M=16, m=8）
   - 理由：中等规模、专业领域、需平衡精度与内存
"""

